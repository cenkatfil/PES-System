     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $series = new Series;
        $series->manu_id = $request->manu_id;
        $series->name = $request->name;
        $series->save();

        return redirect('series/' .$request->manu_id);
    }

